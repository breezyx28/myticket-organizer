const ttStandard = { id: 'tt_std', label: 'Standard', defaultPrice: 120 };
const ttVip = { id: 'tt_vip', label: 'VIP', defaultPrice: 350 };
const ttAcc = { id: 'tt_acc', label: 'Accessibility', defaultPrice: 120 };

function seatsGrid(rows: number, cols: number) {
  const seats = [];
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const accessibility = r === 0 && c < 2;
      const ticketTypeId = accessibility ? ttAcc.id : c > cols - 3 ? ttVip.id : ttStandard.id;
      const price =
        ticketTypeId === ttVip.id ? ttVip.defaultPrice : ticketTypeId === ttAcc.id ? ttAcc.defaultPrice : ttStandard.defaultPrice;
      seats.push({
        id: `s-${r}-${c}`,
        row: r,
        col: c,
        ticketTypeId,
        price,
        accessibility,
      });
    }
  }
  return seats;
}

